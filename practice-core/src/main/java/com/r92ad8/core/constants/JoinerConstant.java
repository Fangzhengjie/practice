package com.r92ad8.core.constants;

import com.google.common.base.Joiner;

/**
 * 字符串连接符
 *
 * @author fangzhengjie
 * @date 2019-11-15
 */
public class JoinerConstant {

    /**
     * 下划线
     */
    public static final String UNDERLINE = "_";

    /**
     * 等于
     */
    public static final String EQUAL = "=";

    /**
     * 加
     */
    public static final String PLUS = "+";

    /**
     *
     */
    public static final String BAR = "|";

    /**
     * 冒号
     */
    public static final String COLON = ":";

    /**
     * 分好
     */
    public static final String SEMICOLON = ";";

    /**
     * 逗号
     */
    public static final String COMMA = ",";

    /**
     * #
     */
    public static final String SHARP = "#";

    /**
     * *号
     */
    public static final String ASTERISK = "*";

    /**
     * .号
     */
    public static final String DOT = ".";

    /**
     * .号
     */
    public static final String AMPERSAND = "&";

    /**
     * 左括号
     */
    public static final String LEFT_BRACKET = "(";

    /**
     * 右括号
     */
    public static final String RIGHT_BRACKET = ")";

    /**
     * 右括号
     */
    public static final String QUOTE = "\"";

    /**
     * 下划线拼接
     */
    public static final Joiner UNDERLINE_JOINER = Joiner.on(UNDERLINE);
    /**
     * 逗号拼接
     */
    public static final Joiner COMMA_JOINER = Joiner.on(COMMA);
    /**
     * #拼接
     */
    public static final Joiner SHARP_JOINER = Joiner.on(SHARP);
    /**
     * *号拼接
     */
    public static final Joiner ASTERISK_JOINER = Joiner.on(ASTERISK);
    /**
     * .号拼接
     */
    public static final Joiner DOT_JOINER = Joiner.on(DOT);
    /**
     * &号拼接
     */
    public static final Joiner AMPERSAND_JOINER = Joiner.on(AMPERSAND);
}
